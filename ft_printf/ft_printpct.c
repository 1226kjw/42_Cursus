#include "ft_printf.h"

int		ft_printpct(t_fm f)
{
	int		n;

	f.sp = 0;
	f.plus = 0;
	n = f.len > 1 ? f.len : 1;
	applynprint("%", &n, f, 0);
	return (n);
}
